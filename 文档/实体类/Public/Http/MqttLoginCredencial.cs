namespace InstantMessaging.Core.Models.Public.Http
{
    public class MqttLoginCredential
    {
        /// <summary>
        /// mqtt 连接的客户端id
        /// </summary>
        public string ClientId { get; set; }
        /// <summary>
        /// mqtt 连接的账户名
        /// </summary>
        public string UserId { get; set; }
        /// <summary>
        /// mqtt 登陆密码
        /// </summary>
        public string Password { get; set; }
        /// <summary>
        /// mqtt 服务器地址 ip+端口
        /// </summary>
        public string Host { get; set; }
        /// <summary>
        /// mqtt ssl 端口
        /// </summary>
        public int SslPort { get; set; }
        /// <summary>
        /// ssl 证书
        /// </summary>
        public byte[] Certification { get; set; }
    }
}
