package usung.com.mqttclient.bean.user;

import java.io.Serializable;

/**
 * Descriptions: 用于保存用户的账号和手势密码，方便判断账号和密码的对应关系
 * Created by fenghui on 2018/4/13.
 */

public class UserAccountAndHandGesturePwd implements Serializable {
    // 账号
    private String accountNum;
    // 手势密码
    private String handGesturePaw;

    public String getAccountNum() {
        return accountNum;
    }

    public void setAccountNum(String accountNum) {
        this.accountNum = accountNum;
    }

    public String getHandGesturePaw() {
        return handGesturePaw;
    }

    public void setHandGesturePaw(String handGesturePaw) {
        this.handGesturePaw = handGesturePaw;
    }
}
